import { useLocation, useNavigate } from 'react-router';
import type { NavTab } from '../types';

interface NavRailItemProps {
  tab: NavTab;
  isCollapsed: boolean;
}

/**
 * Single nav rail item — icon + label, with active state and locked badge.
 */
export default function NavRailItem({ tab, isCollapsed }: NavRailItemProps) {
  const location = useLocation();
  const navigate = useNavigate();

  const isActive = location.pathname === tab.path ||
    (tab.path !== '/' && location.pathname.startsWith(tab.path));

  const handleClick = () => {
    if (!tab.locked) {
      navigate(tab.path);
    }
  };

  return (
    <div
      className={`nav-rail-item ${isActive ? 'nav-rail-item--active' : ''} ${tab.locked ? 'nav-rail-item--locked' : ''}`}
      onClick={handleClick}
      role="button"
      tabIndex={tab.locked ? -1 : 0}
      onKeyDown={(e) => e.key === 'Enter' && handleClick()}
      title={isCollapsed ? tab.label : undefined}
    >
      <div className="nav-rail-item__icon">
        <NavIcon name={tab.icon} />
      </div>
      <span className="nav-rail-item__label">{tab.label}</span>
      {tab.lockedBadge && !isCollapsed && (
        <span className="nav-rail-item__badge">{tab.lockedBadge}</span>
      )}
    </div>
  );
}

